package JavaOperators;

public class RelationalOperator {
    public static void main(String[] args) {
        int firstValue = 500;
        int secondValue = 600;

        System.out.println("Equal checking ====>"+ (firstValue==secondValue));
        System.out.println("Not Equal checking===>"+(firstValue != secondValue));
        System.out.println("Greater then checking===>" + (firstValue > secondValue));
    }
}

